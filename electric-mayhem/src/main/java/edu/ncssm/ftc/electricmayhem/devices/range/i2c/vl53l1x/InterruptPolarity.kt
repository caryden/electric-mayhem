package edu.ncssm.ftc.electricmayhem.devices.range.i2c.vl53l1x

/**
 * Enum representing the polarity of an interrupt signal.
 */
enum class InterruptPolarity {
    /**
     * Represents an interrupt signal that is active when high.
     */
    ACTIVE_HIGH,

    /**
     * Represents an interrupt signal that is active when low.
     */
    ACTIVE_LOW
}
